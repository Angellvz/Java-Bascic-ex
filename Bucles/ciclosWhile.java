public class ciclosWhile {//WHILE FUNCIONA CON CONDICIONES
    public static void main(String[] args) {
        int i=1;
        while(i<=10){//mientras la variable i sea menor a 10
            System.out.println(i);//se imrpime i
            i++;// se le suma 1 a i
        }
        //cuando la condicion deje de cumplerse se termina el while
    }
}
